# 05 - Complex data types

fruitlist = ["apple", "banana", "cherry"]
print(fruitlist)
print (type(fruitlist))

fruittuple = ("apple", "banana", "cherry")
print(fruittuple)
print (type(fruittuple))

thisset = {"apple", "banana", "cherry"}
print (type(thisset))

